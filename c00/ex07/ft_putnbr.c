#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	min;

	if (1 << 31 == nb)
	{
		ft_putchar('-');
		min = ~ (long)(1 << 31) + 1;
		ft_putnbr((int)(min / 10));
		ft_putchar(min % 10 + 48);
	}
	else if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nb);
	}
	else if (nb > 9)
	{	
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + 48);
	}
	else
		ft_putchar(nb + 48);
}
