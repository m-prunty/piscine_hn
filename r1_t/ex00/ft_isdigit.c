int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
	{
		return (1);
	}
	else
	{
		while (str[i] >= '0' && str[i] <= '9')
		{
			i++;
			if (str[i] == '\0')
			{
				return (1);
			}
		}
		return (0);
	}
}
